							<p>Suparna has earned numerous awards, music competitions and appreciations while performing in different cultural programs of Bengali, Hindi , Telugu, and Gujrat community in different parts of USA like Boston, Dayton, Cincinnati, and Louisville, Connecticut</p>
							<p>Suparna did release her most recent debut music album “Bhalobasar Rajarani” with most prominent playback singer of Kolkata, Rupankar Bagchi. Sony DADC, Mumbai  did release this debut album on May 2018.</p>
							<p>Before that Suparna’s had hert duet folk album “Sona Bondhu” launched at Kolakata press club by Raaga Music, Kolkata on Dec 2016.</p>
							<p>Suparna’s first ever solo album “Bandidni” was released back on 2014, which was a cover song album of Smt. Sandhya Mukherjee's songs. Raaga Music of Kolkata did release this album.</p>

