							<h2 class="section_header with_line">Biography</h2>
							<p class="fontsize_20">Suparna Biswas, a mellifluous singer, with expertise in vocal presentation of semi-classical, contemporary, Rabindra Sangeet, folk, band songs, old and modern Bengali, Hindi Bollywood songs. Suparna has the ability to invoke a soulful and mesmerizing experience for her audience through her voice.</p>

							<p>Suparna Biswas did her schooling in different parts of West Bengal because of her father’s transferable job and finally she did her graduation in electronics and telecommunication engineering from B.E College (Indian Institute of Engineering Science and Technology, Shibpur), Howrah.  Later on she started her professional career as senior software engineer in USA.</p>
							<p>Suparna Biswas comes from a rich musical background. Both her parents are very much culturally inclined. Suparna’s passion in music was recognized by her mother at early age of six. It was she who had inspired her to cultivate her talent in music. Suparna started learning music from her first guru – her mother. She then began her classical training from Sri Taru Raykot of Jalpaiguri, WB.</p>
							<p>From her early age Suparna started taking active participation in music competition, in cultural celebrations and continued showing her musical talent during engineering college days. After coming to USA as a software engineer she again started her perusing her passion, music.</p>
						</div>
